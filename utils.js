//DEBOUNCING FUNCTION
//WAITING A CERTAIN AMOUNT OF TIME AFTER THE LAST EVENT
//BEFORE DOING SOMETHING 
const debounce = (func, delay = 1000) => {
  let timeoutId;
  return (...args) => {
    if (timeoutId) {
      clearTimeout(timeoutId);
    }
    timeoutId = setTimeout(() => {
      func.apply(null, args);
    }, delay);
  };
};